from pathlib import Path

"""
Скрипт проверяет все подпапки в текущей директории.
Если в подпапке нет папки "Замечания", создает ее.
Если папка "Замечания" уже существует, пропускает эту подпапку.
"""

# Получаем текущую директорию
current_dir = Path('.')

# Перебираем все объекты в текущей директории
for item in current_dir.iterdir():
    # Проверяем, что это директория (папка)
    if item.is_dir():
        # Проверяем, есть ли подпапка "Замечания"
        remarks_dir = item / "Замечания"
        if not remarks_dir.exists():
            # Если нет - создаем
            remarks_dir.mkdir()
            print(f"Создана папка 'Замечания' в {item}")
        else:
            # Если есть - пропускаем
            print(f"Папка 'Замечания' уже существует в {item}, пропускаем")
